#include <iostream>
using namespace std;

int main()
{
    // 引用-------------------------------------------------------------
    int a;      //与a相比下面 两个变量属于复合类型（类就修饰符+变量名）
    int &n = a; //给a取一个别名（&属于声明的一部分 int &）(类型修饰符 * &)
    //int &m;   //报错，别名必须在定义时初始化
    int *p = &a; //指针（*属于声明的一部分 int &）(类型修饰符 * &)
    int *&r = p; //指向指针的引用 从右向左读，有助于理解这个复杂的类型

    // const---------------------------------------------------------------
    
    return 0;
}